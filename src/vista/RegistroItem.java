    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(txtCodigo.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Codigo valido");
            txtCodigo.requestFocusInWindow();
            return;
        }
        
        
        if(txtNombre.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresar un nombre del item");
            txtNombre.requestFocusInWindow();
            return;
        }
        
        if(txtTipo.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresar un tipo del item");
            txtTipo.requestFocusInWindow();
            return;
        }
        if(txtCantidad.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresar un tipo del item");
            txtCantidad.requestFocusInWindow();
            return;
        }
        
        item nuevoItem = new item(txtCodigo.getText(), txtNombre.getText(), txtTipo.getText(),txtCantidad.getText());
        
        JOptionPane.showMessageDialog(this,"Item registrado con exito."+ nuevoItem.getAumento());
        
        frmPrincipal miLogin = new frmPrincipal();
        miLogin.setVisible(true);
        setVisible(false);
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

